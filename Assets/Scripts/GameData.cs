using UnityEngine;

public class GameData : MonoBehaviour
{
    public static GameData instance;
    public int playerIndex = 0;

    void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
